class Bullet{
    constructor(x,y,tx,ty){
    this.x = x;
    this.y = y;
    this.dir = tx - x;
    this.xdir = Math.abs(tx - x);
    this.ydir = Math.abs(ty - y);
    this.xmod = this.xdir/(this.xdir+this.ydir);
    this.ymod = this.ydir/(this.xdir+this.ydir);
    this.speed = 3;
    this.enabled = true;
    //console.log(this.x);
    //console.log(this.y);
    //console.log(tx);
    //console.log(ty);
   // console.log(this.xmod);
    //console.log(this.ymod);

    }
    draw(){
        ctx.fillStyle = "grey";
        ctx.fillRect(this.x,this.y,10,10); 
    }
    undraw(){
        ctx.fillStyle = "black";
        ctx.fillRect(this.x,this.y,10,10); 
    }
    update() {
        if (this.enabled)
        {
            if (this.dir > 0) {
                this.x = this.x + (this.xmod * this.speed);
            } else {
                this.x = this.x - (this.xmod * this.speed);
            }

            this.y = this.y - (this.ymod * this.speed);
            this.draw();
        }
        
        
        

        

    }

}

